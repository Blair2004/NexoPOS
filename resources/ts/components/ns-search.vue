<template>
    <div class="ns-search">
        <div class="input-group info border-2">
            <input type="text" v-model="searchText" class="p-2 w-full outline-none" :placeholder="placeholder || __( 'Search...' )" id="">
        </div>
        <div class="relative">
            <div class="w-full absolute shadow-lg">
                <ul class="ns-vertical-menu" v-if="results.length > 0 && searchText.length > 0">
                    <li class="border-b p-2 cursor-pointer" v-for="( result, index ) of results" @click="selectOption( result )" :key="index">
                        {{ getLabel(result) }}
                    </li>
                </ul>
            </div>
        </div>
    </div>
</template>
<script lang="ts">
import { __ } from '~/libraries/lang';
import { nsHttpClient, nsSnackBar } from '~/bootstrap';
export default {
    name: 'ns-search',
    props: [ 'url', 'placeholder', 'value', 'label', 'method', 'searchArgument' ],
    data() {
        return {
            searchText: '',
            searchTimeout: null,
            results: [],
        }
    },
    methods: {
        __,
        selectOption( result ) {
            this.$emit( 'select', result );
            this.searchText     =   '';
            this.results        =   [];
        },
        getLabel( value: Function | string ): string {
            if (typeof this.label === 'function') return this.label(value);
            return value[label];
        }
    },
    watch: {
        searchText() {
            clearTimeout( this.searchTimeout );
            this.searchTimeout  =   setTimeout( () => {
                if ( this.searchText.length > 0 ) {
                    nsHttpClient[ this.method || 'post' ]( this.url, { [ this.searchArgument || 'search' ] : this.searchText })
                        .subscribe({
                            next: results => {
                                this.results    =   results;
                            },
                            error: error => {
                                nsSnackBar.error( error.message || __( 'An unexpected error occurred.' ) ).subscribe();
                            }
                        })
                }
            }, 1000 );
        }
    },
    mounted() {
        // ...
    }
}
</script>
